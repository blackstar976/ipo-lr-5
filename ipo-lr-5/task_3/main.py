# Открываем файл для чтения
with open('text.txt', 'r', encoding='utf-8') as f:
    # Читаем содержимое файла
    text = f.read()
    
    # Разбиваем текст на слова (разделитель - пробелы)
    words = text.split()
    
    # Подсчитываем количество слов
    count = len(words)

# Выводим результат
print(f"Количество слов: {count}")