# Получаем строку от пользователя
text = input("Введите текст: ")

# Приводим текст к нижнему регистру для регистронезависимого поиска
text_lower = text.lower()

# Разбиваем текст на слова (разделитель - пробел)
words = text_lower.split()

# Инициализируем счетчик
count = 0

# Перебираем все слова в списке
for word in words:
    # Проверяем, равно ли текущее слово 'python'
    if word == 'python':
        count += 1  # Увеличиваем счетчик

# Выводим результат
print(f"Слово 'Python' встречается {count} раз(а)")